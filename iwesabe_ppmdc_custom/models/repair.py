from odoo import models, fields


class Repair(models.Model):
    _inherit = 'repair.order'

    repair_eqp_id = fields.Many2one('maintenance.equipment', string='Equipment')
    product_id = fields.Many2one('product.product', required=False)
    product_qty = fields.Float('Product Quantity', required=False)
    product_uom = fields.Many2one('uom.uom', required=False)
